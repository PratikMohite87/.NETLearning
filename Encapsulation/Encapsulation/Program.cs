// The process of binding or grouping the State (Data Members) and Behaviour (Member Functions) together into a single unit (i.e. class, interface, struct, etc)
//      where Data Members are private and can be only accessed through setters & getters is called encapsulation in C#.
// The Encapsulation Principle ensures that the state and behavior of a unit cannot be accessed directly from other units.

// Making varibles in class private and making them accessible through public setters and getters is called Data Encapsulation or Data Hiding.

// Advantages of Encapsulation in C#:
//      Protection of data: You can validate the data before storing it in the variable.
//      Achieving Data Hiding: The user will have no idea about the inner implementation of the class.
//      Security: The encapsulation Principle helps to secure our code since it ensures that other units(classes, interfaces, etc) can not access the data directly.
//      Flexibility: The encapsulation Principle in C# makes our code more flexible, which in turn allows the programmer to change or update the code easily.
//      Control: The encapsulation Principle gives more control over the data stored in the variables. For example, we can control the data by validating whether the data is good enough to store in the variable.


        // Stack represents a last-in, first out collection of object.
        // It is used when you need a last-in, first-out access to items.
        // The capacity of a Stack is the number of elements the Stack can hold.
        // As elements are added to a Stack, the capacity is automatically increased as required through reallocation.
        // If Count is less than the capacity of the stack, Push is an O(1) operation.
        // If the capacity needs to be increased to accommodate the new element, Push becomes an O(n) operation, where n is Count.Pop is an O(1) operation.
        // Stack accepts null as a valid value and allows duplicate elements.


